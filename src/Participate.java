    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here: start
        String id = jTextField1.getText();
        if(id.length() == 0)
        {
            JOptionPane.showMessageDialog(null,"Cannot be blank!");            
        }else{
            String query = "SELECT survey_id,survey_name,num_questions FROM survey_details WHERE survey_id = "+id+" AND created_by != "+userid+";";
            try{
                ResultSet rs1 = stmt.executeQuery(query);
                if(rs1.next())
                {
                    surveyid = id;
                    num_questions = rs1.getInt("num_questions");
                    survey_name = rs1.getString("survey_name");
                    jLabel1.setText(survey_name);
                    String query2 = "SELECT * FROM questions WHERE survey_id = "+id+";";
                    try{
                        ResultSet rs = stmt.executeQuery(query2);
                        while(rs.next())
                        {
                            ArrayList<String> q = new ArrayList<>();
                            q.add(rs.getString("question"));
                            q.add(rs.getString("opt1"));
                            q.add(rs.getString("opt2"));
                            q.add(rs.getString("opt3"));
                            q.add(rs.getString("opt4"));
                            q.add(Integer.toString(rs.getInt("id"))); // check
                            questions.add(q);
                            //q.clear();
                        }
                        jButton1.setEnabled(false);
                        jButton2.setEnabled(true);
                        rs.close();
                    }catch(Exception e2)
                    {
                        e2.printStackTrace();
                        System.out.println(e2);
                        setVisible(false);
                        JOptionPane.showMessageDialog(null, "Error!Could not fetch questions:");
                    }
                    
                    getNextQuestion();
                }else{
                    System.out.println("Not available! Please re-enter id:");
                    JOptionPane.showMessageDialog(null, "Not available! Please re-enter id:");
                }
                
                rs1.close();
            }catch(Exception e)
            {
                e.printStackTrace();
                System.out.println(e);
                JOptionPane.showMessageDialog(null, e);
                setVisible(false);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here: next / submit
        if(num_questions == 0){
            // submit
            submitQuestion();
            String query1= "INSERT INTO log(userid,surveyid) VALUES("+userid+","+surveyid+");";
            String query2 = "UPDATE survey_details SET num_participants  = num_participants  + 1 WHERE survey_id = "+surveyid+";";
            try{
                stmt.executeUpdate(query1);
                stmt.executeUpdate(query2);
                for(ArrayList<String> ans : answers)
                {
                    String query3 = "INSERT INTO log_questions VALUES("+userid+","+ans.get(0)+","+ans.get(1)+",'"+ans.get(2)+"');";
                    String query4 = "UPDATE questions SET cnt"+ans.get(1)+" = cnt"+ans.get(1)+" + 1 WHERE id = "+ans.get(0)+";";
                    try{
                        stmt.executeUpdate(query3);
                        stmt.executeUpdate(query4);
                    }catch(Exception eb)
                    {
                        eb.printStackTrace();
                        System.out.println(eb);
                        JOptionPane.showMessageDialog(null, eb);
                        setVisible(false);
                    }
                }
                JOptionPane.showMessageDialog(null, "Completed Survey Succesfully! Thankyou!");
                setVisible(false);
            }catch(Exception ea)
            {
                ea.printStackTrace();
                System.out.println(ea);
                JOptionPane.showMessageDialog(null, ea);
                setVisible(false);
            }

        }else{
            submitQuestion();
            getNextQuestion();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

