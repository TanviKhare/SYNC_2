    void getNextQuestion(){
        System.out.println(questions);
        buttonGroup1.clearSelection();
        ArrayList<String> row = questions.get(questions.size() - num_questions);
        // System.out.println(row);
        curr_ques_id = row.get(5);
        jLabel4.setText(row.get(0));
        jRadioButton1.setText(row.get(1));
        jRadioButton2.setText(row.get(2));
        jRadioButton3.setText(row.get(3));
        jRadioButton4.setText(row.get(4));
        
        jRadioButton1.setActionCommand("1" + row.get(1));
        jRadioButton2.setActionCommand("2" + row.get(2));
        jRadioButton3.setActionCommand("3" + row.get(3));
        jRadioButton4.setActionCommand("4" + row.get(4));
        num_questions--;
        jLabel5.setText(Integer.toString(questions.size() - num_questions) + "/" + Integer.toString(questions.size()));
    }
    
    void submitQuestion()
    {
        ArrayList<String> thisAns = new ArrayList<>();
        String ans = buttonGroup1.getSelection().getActionCommand();
        //System.out.println(buttonGroup1.getSelection());
        thisAns.add(curr_ques_id);        
        thisAns.add(ans.substring(0,1));
        thisAns.add(ans.substring(1,ans.length()));
        answers.add(thisAns);
    }
    
    
