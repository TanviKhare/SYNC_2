    public ViewSurvey(String user) {
        initComponents();
        try{
            con = ConnectionProvider.getConnection();
            stmt = con.createStatement();
        }catch(Exception e){
            e.printStackTrace();
            System.out.println(e);
            JOptionPane.showMessageDialog(null,"Could not start the application!");
        }
        
        String query = "SELECT survey_id,survey_name,num_questions,created_on,num_participants FROM survey_details WHERE created_by = "+user+" ORDER BY created_on DESC;";
        try{
            ResultSet rs = stmt.executeQuery(query);
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(Exception e)
        {
            e.printStackTrace();
            System.out.println(e);
            setVisible(false);
            JOptionPane.showMessageDialog(null,"Error! Could not fetch available surveys!");
        }
    }

